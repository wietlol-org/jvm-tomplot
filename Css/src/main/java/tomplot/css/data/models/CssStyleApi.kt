private fun <T : CssValue> named(name: String): ReadWriteProperty<CssStyleApi, T?> =
	decoratorCache.computeIfAbsent(name) {
		object : ReadWriteProperty<CssStyleApi, T?>
		{
			override fun getValue(thisRef: CssStyleApi, property: KProperty<*>): T? =
				thisRef.properties[name] as T?
			
			override fun setValue(thisRef: CssStyleApi, property: KProperty<*>, value: T?) =
				thisRef.properties.setOrRemove(name, value)
		}
	} as ReadWriteProperty<CssStyleApi, T?>
